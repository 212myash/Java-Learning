class PrimitiveDataExample {
    public static void main(String[] args) {
        int age = 20;
        float marks = 85.5f;
        char grade = 'A';
        boolean status = true;

        System.out.println(age);
        System.out.println(marks);
        System.out.println(grade);
        System.out.println(status);
    }
}
